var getSum = function(a, b) {
    while (b != 0) {
        const carry = (a & b) << 1;//两个数相加后的进位结果
        a = a ^ b;//异或,表示两个数相加后的本位和
        b = carry;
    }
    return a;
};
getSum(5,6)
/*
a=101 b=110
carry=(100)<<1=1000=8
a=011=3
b=1000
carry=0
a=1011=11
b=0
 */
/*
a=11=1011 b=10=1010
进位：carry=(1010)<<1=10100
本位和：a=1
->a=1 b=10100
进位：carry=0
本位和：a=10101
->a=10101 b=0
进位：carry=0
本位和：a=10101
->a=10101 b=0->sum=10101=21
 */
